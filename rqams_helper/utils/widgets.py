from typing import Iterable

from PySide2.QtCore import QPropertyAnimation, QRectF, QSize, Qt, Property, Slot
from PySide2.QtGui import QPainter
from PySide2.QtWidgets import QAbstractButton, QSizePolicy, QMessageBox, QWidget


class MessageBox(QMessageBox):
    def __init__(self, icon, title, text):
        super(MessageBox, self).__init__(icon, title, title)
        self.setButtonText(QMessageBox.Ok, "确认")
        self.setText(text)


class ConfirmMessageBox(MessageBox):
    def __init__(self, title, text):
        super(ConfirmMessageBox, self).__init__(QMessageBox.Information, title, text)
        self.setStandardButtons(QMessageBox.Ok | QMessageBox.Cancel)
        self.setButtonText(QMessageBox.Cancel, "取消")


class DetailMessageBox(MessageBox):
    SHOW_DETAIL_TEXT = "显示详细信息"
    HIDE_DETAIL_TEXT = "隐藏详细信息"

    def __init__(self, icon, title, text, detail):
        super(DetailMessageBox, self).__init__(icon, title, text)
        self.setDetailedText(detail)

    def _detail_button_text_replaced(self, btn):
        if btn.text() == "Show Details...":
            btn.setText(self.SHOW_DETAIL_TEXT)
            return True
        elif btn.text() == "Hide Details...":
            btn.setText(self.HIDE_DETAIL_TEXT)
            return True
        return False

    @Slot()
    def _detailed_button_clicked(self):
        self._detail_button_text_replaced(self.sender())

    def showEvent(self, event):
        for btn in self.buttons():
            if self._detail_button_text_replaced(btn):
                btn.clicked.connect(self._detailed_button_clicked)


class ToggleSwitch(QAbstractButton):
    # implementation based on: https://stackoverflow.com/a/51825815

    def __init__(self, parent=None, track_radius=10, thumb_radius=8):
        super().__init__(parent=parent)
        self.setCheckable(True)
        self.setSizePolicy(QSizePolicy.Fixed, QSizePolicy.Fixed)

        self._track_radius = track_radius
        self._thumb_radius = thumb_radius

        self._margin = max(0, self._thumb_radius - self._track_radius)
        self._base_offset = max(self._thumb_radius, self._track_radius)
        self._end_offset = {
            True: lambda: self.width() - self._base_offset,
            False: lambda: self._base_offset,
        }
        self._offset = self._base_offset

        palette = self.palette()
        if self._thumb_radius > self._track_radius:
            self._track_color = {
                True: palette.highlight(),
                False: palette.dark(),
            }
            self._thumb_color = {
                True: palette.highlight(),
                False: palette.light(),
            }
            self._text_color = {
                True: palette.highlightedText().color(),
                False: palette.dark().color(),
            }
            self._thumb_text = {
                True: '',
                False: '',
            }
            self._track_opacity = 0.5
        else:
            self._thumb_color = {
                True: palette.highlightedText(),
                False: palette.light(),
            }
            self._track_color = {
                True: palette.highlight(),
                False: palette.dark(),
            }
            self._text_color = {
                True: palette.highlight().color(),
                False: palette.dark().color(),
            }
            self._thumb_text = {
                True: '✔',
                False: '✕',
            }
            self._track_opacity = 1

    def _get_offset(self):
        return self._offset

    def _set_offset(self, value):
        self._offset = value
        self.update()

    offset = Property(int, _get_offset, _set_offset)

    def sizeHint(self):  # pylint: disable=invalid-name
        return QSize(
            4 * self._track_radius + 2 * self._margin,
            2 * self._track_radius + 2 * self._margin,
        )

    def setChecked(self, checked):
        super().setChecked(checked)
        self.offset = self._end_offset[checked]()

    def resizeEvent(self, event):
        super().resizeEvent(event)
        self.offset = self._end_offset[self.isChecked()]()

    def paintEvent(self, event):  # pylint: disable=invalid-name, unused-argument
        p = QPainter(self)
        p.setRenderHint(QPainter.Antialiasing, True)
        p.setPen(Qt.NoPen)
        track_opacity = self._track_opacity
        thumb_opacity = 1.0
        text_opacity = 1.0
        if self.isEnabled():
            track_brush = self._track_color[self.isChecked()]
            thumb_brush = self._thumb_color[self.isChecked()]
            text_color = self._text_color[self.isChecked()]
        else:
            track_opacity *= 0.8
            track_brush = self.palette().shadow()
            thumb_brush = self.palette().mid()
            text_color = self.palette().shadow().color()

        p.setBrush(track_brush)
        p.setOpacity(track_opacity)
        p.drawRoundedRect(
            self._margin,
            self._margin,
            self.width() - 2 * self._margin,
            self.height() - 2 * self._margin,
            self._track_radius,
            self._track_radius,
        )
        p.setBrush(thumb_brush)
        p.setOpacity(thumb_opacity)
        p.drawEllipse(
            self.offset - self._thumb_radius,
            self._base_offset - self._thumb_radius,
            2 * self._thumb_radius,
            2 * self._thumb_radius,
        )
        p.setPen(text_color)
        p.setOpacity(text_opacity)
        font = p.font()
        font.setPixelSize(1.5 * self._thumb_radius)
        p.setFont(font)
        p.drawText(
            QRectF(
                self.offset - self._thumb_radius,
                self._base_offset - self._thumb_radius,
                2 * self._thumb_radius,
                2 * self._thumb_radius,
            ),
            Qt.AlignCenter,
            self._thumb_text[self.isChecked()],
        )

    def mouseReleaseEvent(self, event):  # pylint: disable=invalid-name
        super().mouseReleaseEvent(event)
        if event.button() == Qt.LeftButton:
            anim = QPropertyAnimation(self, b'offset', self)
            anim.setDuration(120)
            anim.setStartValue(self.offset)
            anim.setEndValue(self._end_offset[self.isChecked()]())
            anim.start()

    def enterEvent(self, event):  # pylint: disable=invalid-name
        self.setCursor(Qt.PointingHandCursor)
        super().enterEvent(event)


def add_enter_press_event(widgets: Iterable[QWidget], func):
    def make_key_press_event(original_event_func):
        def key_press_event(event):
            if event.key() in {Qt.Key_Return, Qt.Key_Enter}:
                func()
            else:
                original_event_func(event)
        return key_press_event
    for w in widgets:
        w.keyPressEvent = make_key_press_event(w.keyPressEvent)


def disable_widget(widget: QWidget, text: str):
    widget.setDisabled(True)
    widget.setToolTip(text)


def enabled_widget(widget: QWidget):
    widget.setEnabled(True)
    widget.setToolTip("")

